#include "libft.h"

int	ft_isalnum(int d)
{
	if ((d >= 'A' && 'Z' >= d) || (d >= 'a' && 'z' >= d))
	{
		return (1);
	}
	else if (d >= '0' && d <= '9')
		return (1);
	else
		return (0);
}
